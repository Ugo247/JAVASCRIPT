let array = ["a", "b", "c", "d", "e", "f",];
array.push("g", "1", "z");
console.log(array);
// pop = removes an item from the end of an array
array = ["a", "b", "c", "d", "e", "f",];
array.pop();
array.pop();
console.log(array);
// start = removes an item from the start of an array
array = ["a", "b", "c", "d", "e", "f",];
array.shift();
console.log(array);
// unshift = adds item(s) to the start of an array
array = ["a", "b", "c", "d", "e", "f",];
array.unshift("g", "1", "z");
console.log(array);
